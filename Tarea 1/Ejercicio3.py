#Se le solicita al usuario ingresar los 2 primeros Numeros
numero_entero=float(input("Ingrese Numero: "))
numero_entero2=float(input("Ingrese Numero: "))
#Se suman los 2 primeros datos
suma = numero_entero + numero_entero2
print("La suma de los numeros ingresados es: ",suma)
#Se pide un tercer dato
numero_entero3=float(input("Ingrese Numero:"))
#Se opera el resultado de la suma por la multiplicacion del tercer numero
resultado = suma*numero_entero3
print("El resultado de la suma por la multiplicacion es: ",resultado)